<?php 

// === FUNÇÕES NATIVAS DO PHP PARA STRINGS === //

$nome = "Erick";

$nome = str_replace("E", "3", $nome); // -> "str_replace("E", "3", $nome)" trocara o E por 3 na variavel $nome
$nome = str_replace("i", "1", $nome); // -> "str_replace("i", "1", $nome)" trocara o i por 1 na variavel $nome

echo $nome;

?>